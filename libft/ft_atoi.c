#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int n;
	int neg;

	n = 0;
	neg = 0;
	i = 0;
	if (!str)
		return (0);
	while ((str[i] == '\n') || (str[i] == '\t') || (str[i] == '\v') ||
			(str[i] == ' ') || (str[i] == '\f') || (str[i] == '\r'))
		i++;
	if (str[i] == '-')
		neg = 1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] && (str[i] >= '0') && (str[i] <= '9'))
	{
		n *= 10;
		n += (int)str[i] - '0';
		i++;
	}
	return (neg == 1 ? -n : n);
}
