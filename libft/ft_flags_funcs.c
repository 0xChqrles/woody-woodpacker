#include "ft_printf.h"

void	ft_long_overflow(t_pline *pline, char c)
{
	if ((c == 'D' && pline->llid < -9223372036854775807)
	|| (c == 'd' && pline->lid < -9223372036854775807)
	|| (c == 'j' && pline->jid < -9223372036854775807)
	|| (c == 'z' && (long long int)pline->zid < -9223372036854775807))
	{
		ft_putstr("-9");
		if (c == 'D')
			pline->llid = 223372036854775808;
		else if (c == 'd')
			pline->lid = 223372036854775808;
		else if (c == 'j')
			pline->jid = 223372036854775808;
		else
			pline->zid = 223372036854775808;
		pline->count += 2;
	}
}
